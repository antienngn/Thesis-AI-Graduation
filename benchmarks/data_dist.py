import json  
import matplotlib.pyplot as plt  
import numpy as np  
from transformers import AutoTokenizer  
from datasets import load_dataset  
from fastchat.model import get_conversation_template  
import argparse  
import os  
  
def load_lmsys_dataset(tokenizer, num_samples=None):  
    """Load lmsys dataset and extract prompts/completions"""  
    dataset = load_dataset("lmsys/lmsys-chat-1m")['train']  
    if num_samples:  
        dataset = dataset.select(range(min(num_samples, len(dataset))))  
      
    prompts = []  
    completions = []  
      
    for item in dataset:  
        prompt = None  
        for conv in item['conversation']:  
            if conv['role'] == 'user':  
                prompt = conv['content']  
                break  
          
        if prompt:  
            # Format prompt for llama3  
            conv = get_conversation_template(tokenizer.name_or_path)  
            conv.append_message(conv.roles[0], prompt)  
            conv.append_message(conv.roles[1], None)  
            formatted_prompt = conv.get_prompt()  
            prompts.append(formatted_prompt)  
            # Note: lmsys dataset doesn't have completions, using empty for now  
            completions.append("")  
      
    return prompts, completions  
  
def load_sharegpt_dataset(tokenizer, num_samples=None):  
    """Load sharegpt dataset and extract prompts/completions"""  
    with open("ShareGPT_V3_unfiltered_cleaned_split.json", 'r') as f:  
        dataset = json.load(f)  
      
    # Filter conversations with at least 2 turns  
    dataset = [data for data in dataset if len(data["conversations"]) >= 2]  
      
    if num_samples:  
        dataset = dataset[:min(num_samples, len(dataset))]  
      
    prompts = []  
    completions = []  
      
    for data in dataset:  
        prompt = data["conversations"][0]["value"]  
        completion = data["conversations"][1]["value"]  
          
        # Format prompt for llama3  
        conv = get_conversation_template(tokenizer.name_or_path)  
        conv.append_message(conv.roles[0], prompt)  
        conv.append_message(conv.roles[1], None)  
        formatted_prompt = conv.get_prompt()  
          
        prompts.append(formatted_prompt)  
        completions.append(completion)  
      
    return prompts, completions  
  
def load_jsonl_dataset(file_path, tokenizer, num_samples=None):  
    """Load dataset from JSONL file (generated by benchmark_generate_dataset.py)"""  
    prompts = []  
    completions = []  
      
    with open(file_path, 'r', encoding='utf-8') as f:  
        for line in f:  
            data = json.loads(line)  
            prompts.append(data["prompt"])  
            completions.append(data["generated"])  
              
            if num_samples and len(prompts) >= num_samples:  
                break  
      
    return prompts, completions  
  
def calculate_lengths(prompts, completions, tokenizer):  
    """Calculate token lengths for prompts and completions"""  
    prompt_lengths = []  
    output_lengths = []  
      
    # Tokenize all prompts  
    prompt_token_ids = tokenizer(prompts).input_ids  
      
    # Tokenize all completions  
    completion_token_ids = tokenizer(completions).input_ids  
      
    for i in range(len(prompts)):  
        prompt_len = len(prompt_token_ids[i])  
        output_len = len(completion_token_ids[i])  
          
        # Filter out extreme values  
        if prompt_len > 0 and prompt_len < 8192 and output_len > 0 and output_len < 8192:  
            prompt_lengths.append(prompt_len)  
            output_lengths.append(output_len)  
      
    return prompt_lengths, output_lengths  
  
def plot_distributions(prompt_lengths, output_lengths, dataset_name, save_dir):  
    """Create and save distribution plots"""  
    os.makedirs(save_dir, exist_ok=True)  
      
    # Create subplots  
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(15, 6))  
      
    # Input length distribution  
    ax1.hist(prompt_lengths, bins=50, alpha=0.7, color='blue', edgecolor='black')  
    ax1.set_xlabel('Input Token Length')  
    ax1.set_ylabel('Frequency')  
    ax1.set_title(f'{dataset_name} - Input Sequence Length Distribution')  
    ax1.grid(True, alpha=0.3)  
      
    # Add statistics  
    mean_input = np.mean(prompt_lengths)  
    median_input = np.median(prompt_lengths)  
    ax1.axvline(mean_input, color='red', linestyle='--', label=f'Mean: {mean_input:.1f}')  
    ax1.axvline(median_input, color='green', linestyle='--', label=f'Median: {median_input:.1f}')  
    ax1.legend()  
      
    # Output length distribution  
    ax2.hist(output_lengths, bins=50, alpha=0.7, color='orange', edgecolor='black')  
    ax2.set_xlabel('Output Token Length')  
    ax2.set_ylabel('Frequency')  
    ax2.set_title(f'{dataset_name} - Output Sequence Length Distribution')  
    ax2.grid(True, alpha=0.3)  
      
    # Add statistics  
    mean_output = np.mean(output_lengths)  
    median_output = np.median(output_lengths)  
    ax2.axvline(mean_output, color='red', linestyle='--', label=f'Mean: {mean_output:.1f}')  
    ax2.axvline(median_output, color='green', linestyle='--', label=f'Median: {median_output:.1f}')  
    ax2.legend()  
      
    plt.tight_layout()  
    plt.savefig(f'{save_dir}/{dataset_name}_length_distributions.png', dpi=300, bbox_inches='tight')  
    plt.close()  
      
    # Print statistics  
    print(f"\n{dataset_name} Dataset Statistics:")  
    print(f"Input lengths - Mean: {mean_input:.1f}, Median: {median_input:.1f}, "  
          f"Min: {min(prompt_lengths)}, Max: {max(prompt_lengths)}")  
    print(f"Output lengths - Mean: {mean_output:.1f}, Median: {median_output:.1f}, "  
          f"Min: {min(output_lengths)}, Max: {max(output_lengths)}")  
  
def main():  
    parser = argparse.ArgumentParser(description="Analyze sequence length distributions")  
    parser.add_argument("--model", type=str, default="meta-llama/Meta-Llama-3-8B-Instruct",  
                        help="Model name for tokenizer")  
    parser.add_argument("--num-samples", type=int, default=10000,  
                        help="Number of samples to analyze")  
    parser.add_argument("--save-dir", type=str, default="length_analysis",  
                        help="Directory to save plots")  
    parser.add_argument("--lmsys-file", type=str,   
                        help="Path to lmsys JSONL file (if not provided, loads from huggingface)")  
    parser.add_argument("--sharegpt-file", type=str,  
                        help="Path to sharegpt JSONL file (if not provided, loads from local file)")  
      
    args = parser.parse_args()  
      
    # Load tokenizer  
    tokenizer = AutoTokenizer.from_pretrained(args.model)  
      
    # Analyze LMSYS dataset  
    if args.lmsys_file and os.path.exists(args.lmsys_file):  
        print(f"Loading LMSYS dataset from {args.lmsys_file}")  
        lmsys_prompts, lmsys_completions = load_jsonl_dataset(args.lmsys_file, tokenizer, args.num_samples)  
    else:  
        print("Loading LMSYS dataset from HuggingFace")  
        lmsys_prompts, lmsys_completions = load_lmsys_dataset(tokenizer, args.num_samples)  
      
    lmsys_input_lens, lmsys_output_lens = calculate_lengths(lmsys_prompts, lmsys_completions, tokenizer)  
    plot_distributions(lmsys_input_lens, lmsys_output_lens, "LMSYS", args.save_dir)  
      
    # Analyze ShareGPT dataset  
    if args.sharegpt_file and os.path.exists(args.sharegpt_file):  
        print(f"Loading ShareGPT dataset from {args.sharegpt_file}")  
        sharegpt_prompts, sharegpt_completions = load_jsonl_dataset(args.sharegpt_file, tokenizer, args.num_samples)  
    else:  
        print("Loading ShareGPT dataset from local file")  
        sharegpt_prompts, sharegpt_completions = load_sharegpt_dataset(tokenizer, args.num_samples)  
      
    sharegpt_input_lens, sharegpt_output_lens = calculate_lengths(sharegpt_prompts, sharegpt_completions, tokenizer)  
    plot_distributions(sharegpt_input_lens, sharegpt_output_lens, "ShareGPT", args.save_dir)  
      
    # Create comparison plot  
    fig, ((ax1, ax2), (ax3, ax4)) = plt.subplots(2, 2, figsize=(15, 12))  
      
    # LMSYS input  
    ax1.hist(lmsys_input_lens, bins=50, alpha=0.7, color='blue', edgecolor='black')  
    ax1.set_xlabel('Input Token Length')  
    ax1.set_ylabel('Frequency')  
    ax1.set_title('LMSYS - Input Length')  
    ax1.grid(True, alpha=0.3)  
      
    # LMSYS output  
    ax2.hist(lmsys_output_lens, bins=50, alpha=0.7, color='orange', edgecolor='black')  
    ax2.set_xlabel('Output Token Length')  
    ax2.set_ylabel('Frequency')  
    ax2.set_title('LMSYS - Output Length')  
    ax2.grid(True, alpha=0.3)  
      
    # ShareGPT input  
    ax3.hist(sharegpt_input_lens, bins=50, alpha=0.7, color='green', edgecolor='black')  
    ax3.set_xlabel('Input Token Length')  
    ax3.set_ylabel('Frequency')  
    ax3.set_title('ShareGPT - Input Length')  
    ax3.grid(True, alpha=0.3)  
      
    # ShareGPT output  
    ax4.hist(sharegpt_output_lens, bins=50, alpha=0.7, color='red', edgecolor='black')  
    ax4.set_xlabel('Output Token Length')  
    ax4.set_ylabel('Frequency')  
    ax4.set_title('ShareGPT - Output Length')  
    ax4.grid(True, alpha=0.3)  
      
    plt.tight_layout()  
    plt.savefig(f'{args.save_dir}/dataset_comparison.png', dpi=300, bbox_inches='tight')  
    plt.close()  
      
    print(f"\nPlots saved to {args.save_dir}/")  
  
if __name__ == "__main__":  
    main()